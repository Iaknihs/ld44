def count_neighbors(field, i, j):
    n = 0
    for imod in [-1, 0, 1]:
        for jmod in [-1, 0, 1]:
            if 0 <= i + imod < len(field) and 0 <= j + jmod < len(field[0]) and not (imod == jmod and imod == 0):
                if field[i+imod][j+jmod] == 1:
                    n += 1
    # print(n)
    return n

	
